    private void registerResourceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerResourceBtnActionPerformed

        int copies = (int) resourceCopies.getValue();
        
        ResourceController resourceController = new ResourceController(
                resourceType.getSelectedItem().toString(),
                resourceId.getText(),
                resourceTitle.getText(),
                copies
        );
        
        if (resourceController.validateInput()){
            try{
                resourceController.connectToDB();
                resourceController.registerUser();
                JOptionPane.showMessageDialog(null, "SUCCESS, " + resourceTitle.getText() + " added Successfully", "Success?", JOptionPane.INFORMATION_MESSAGE);
                clearInputs();
                this.dispose();
                new RegisterResourceView().main(null);

            }catch(Exception e){
                clearInputs();
                JOptionPane.showMessageDialog(null, "Error adding resource \n Technical errro details: " + e.toString(), "error msg", JOptionPane.ERROR_MESSAGE);
            }

        }
        
    }//GEN-LAST:event_registerResourceBtnActionPerformed

