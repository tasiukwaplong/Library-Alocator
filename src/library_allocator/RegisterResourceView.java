    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int selectedRowIndex = resourceTable.getSelectedRow();
        if (selectedRowIndex >= 0){
            Object resource_id = (Object) resourceTable.getModel().getValueAt(selectedRowIndex, 2);
            Object resource_title = (Object) resourceTable.getModel().getValueAt(selectedRowIndex, 3);
            Object resource_copies = (Object) resourceTable.getModel().getValueAt(selectedRowIndex, 4);
            Object resource_type = (Object) resourceTable.getModel().getValueAt(selectedRowIndex, 1);
            
            resourceId.setText(resource_id.toString());
            resourceTitle.setText(resource_title.toString());
            resourceCopies.setValue(resource_copies);
            resourceType.setSelectedItem(resource_type);

       // System.out.print(selectedRowIndex + "..........."+ resource_id + "......"+resource_title +"......."+resource_copies);
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedRowIndex = resourceTable.getSelectedRow();
        
        if(selectedRowIndex >= 0 ){
            Object title = (Object) resourceTable.getModel().getValueAt(selectedRowIndex, 3);

             
             int confirmAction =  JOptionPane.showConfirmDialog(null, "Are you sure this resource is no more available?", "Confirm action", JOptionPane.YES_NO_OPTION);
             
             if(confirmAction == 0){
                try{
                   ResourceController resourceController = new ResourceController(title.toString());
                   resourceController.connectToDB();
                   resourceController.deleteResource();
                 //  JOptionPane.showMessageDialog(null, "Resource deleted..!!", "Success", JOptionPane.PLAIN_MESSAGE);
             
                   this.dispose();
                   new RegisterResourceView().main(null);
                   
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e, "An error has occured", JOptionPane.ERROR_MESSAGE);
                }
                
            }
            
            }
    }//GEN-LAST:event_deleteBtnActionPerformed

